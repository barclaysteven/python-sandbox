# Working with string functions

name = "steven"
print("Original String: " + name)
print("Capitalized String: " + name.capitalize())
print("Is the string alpha-numeric: " + str(name.isalnum()))
print("Number of 'e's in the string: " + str(name.count('e')))
print("Length of the string: " + str(name.__len__()))
