# Simple Math calculations

x = 2
y = 5.0

add_result = x + y
sub_result = x - y
multi_result = x * y
divid_result = x / y
int_divid_result = x // y
modulos_result = x % y

print("Addition result " + str(add_result))
print("Subtraction result " + str(sub_result))
print("Multiplication Result " + str(multi_result))
print("Division Result " + str(divid_result))
print("Integer Division Result " + str(int_divid_result))
print("Modulos Result " + str(modulos_result))
